public class OperatorPrecedence {

	public static void main(String[] args) {
		
		 System.out.println(2+0+1+6+"Shreyas");
		 
	     System.out.println("Shreyas"+2+0+1+6);
	     
	     System.out.println(2+0+1+5+"Shreyas"+2+0+1+6);
	     
	     System.out.println(2+0+1+5+"Shreyas"+(2+0+1+6));
	}
}
